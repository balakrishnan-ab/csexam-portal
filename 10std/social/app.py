import streamlit as st
import folium
from streamlit_folium import st_folium

st.title("📍 10-ஆம் வகுப்பு சமூக அறிவியல் வரைபடப் பயிற்சி")

# வரைபடத் தரவுகள் (தமிழ் குறிப்புகளுடன்)
map_data = [
    {"பெயர்": "வேதாரண்யம்", "lat": 10.3758, "lon": 79.8450, "தகவல்": "தமிழகத்தில் உப்பு சத்தியாகிரகம் நடைபெற்ற இடம்."},
    {"பெயர்": "ஜான்சி", "lat": 25.4484, "lon": 78.5685, "தகவல்": "1857 பெரும் புரட்சியின் முக்கிய மையம்."},
    {"பெயர்": "காவிரி ஆறு", "lat": 11.3410, "lon": 77.1000, "தகவல்": "தமிழகத்தின் மிக முக்கியமான வாழ்வாதார ஆறு."}
]

st.write("வரைபடத்தில் உள்ள குறியீடுகளைச் சொடுக்கி அதன் சிறப்புகளைத் தெரிந்து கொள்ளுங்கள்.")

# இந்திய வரைபடத்தை உருவாக்குதல்
m = folium.Map(location=[20.5937, 78.9629], zoom_start=5)

# வரைபடத்தில் இடங்களைச் சேர்த்தல்
for place in map_data:
    folium.Marker(
        location=[place["lat"], place["lon"]],
        popup=f"<b>{place['பெயர்']}</b><br>{place['தகவல்']}",
        tooltip=place["பெயர்"]
    ).add_to(m)

# Streamlit-ல் வரைபடத்தைக் காட்டுதல்
st_folium(m, width=700, height=500)

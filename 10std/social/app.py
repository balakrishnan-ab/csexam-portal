import streamlit as st

# தலைப்பு
st.title("📍 10-ம் வகுப்பு வரைபடப் பயிற்சி")
st.write("சமூக அறிவியல் பாடத்திற்கான முக்கிய இடங்களைக் கண்டறியவும்.")

# வரைபட இடங்களின் தரவு (எடுத்துக்காட்டு)
locations = {
    "இந்தியா": {
        "வரலாறு": ["மீரட்", "ஜான்சி", "சௌரி சௌரா", "வேதாரண்யம்", "தண்டி"],
        "புவியியல்": ["காரகோரம் மலைத்தொடர்", "தார் பாலைவனம்", "காவிரி ஆறு", "கொச்சின் துறைமுகம்"]
    },
    "உலகம்": {
        "வரலாறு": ["பிரிட்டன்", "ஜெர்மனி", "இத்தாலி", "ஜப்பான்", "மொராக்கோ"]
    }
}

# தேர்வு செய்யும் பகுதி
map_type = st.selectbox("வரைபடத்தைத் தேர்ந்தெடுக்கவும்:", ["இந்தியா", "உலகம்"])
category = st.radio("பகுதி:", list(locations[map_type].keys()))

# இடங்களைக் காண்பித்தல்
st.subheader(f"{map_type} - {category} முக்கிய இடங்கள்")
selected_places = locations[map_type][category]

for place in selected_places:
    if st.checkbox(place):
        st.success(f"நீங்கள் '{place}' இடத்தைப் பயிற்சி செய்துவிட்டீர்கள்!")

# வரைபடப் படம் (உங்களிடம் உள்ள வரைபடப் படங்களை இங்கு இணைக்கலாம்)
st.info("குறிப்பு: இந்தப் பட்டியலில் உள்ள இடங்களை உங்கள் வரைபடத்தில் குறித்துப் பழகவும்.")
